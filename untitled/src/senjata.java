import java.util.*;
public class senjata {
    public int jmlPeluru;
    public int jenis;
    public String nama;
    public String berat;
    public String warna;

    public int getJmlPeluru() {
        return jmlPeluru;
    }

    public void setJmlPeluru(int jmlPeluru) {
        this.jmlPeluru = jmlPeluru;
    }

    public int getJenis() {
        return jenis;
    }

    public void setJenis(int jenis) {
        this.jenis = jenis;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getBerat() {
        return berat;
    }

    public void setBerat(String berat) {
        this.berat = berat;
    }

    public String getWarna() {
        return warna;
    }

    public void setWarna(String warna) {
        this.warna = warna;
    }
}
